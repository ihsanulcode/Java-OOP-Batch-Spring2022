
public class Comparison {

	public static void main(String[] args) {
		//comparison operator
		
		int a = 1;
		int b = 2;
		
		System.out.println(a==b);
		System.out.println(a>b);
		System.out.println(a<b);
		
		//logical operators
		int x = 5;
		int y = 10;
		
		System.out.println(x>0 && x<11);
		System.out.println(x>=1 && x<=10);
		System.out.println(y>=100);
		
		System.out.println(y>50 || y<50); //0 or 1 = 1 = true
		System.out.println(!(x==5)); //not gate !

	}

}
