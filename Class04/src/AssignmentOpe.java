
public class AssignmentOpe {

	public static void main(String[] args) {
		//Assignment operator
		
		int n = 2;
		System.out.println(n);
		n += 2; //n = n+2;
		System.out.println(n);
		
		n -= 2; //n = n-2;
		System.out.println(n);
		
		n *= 2; //n = n*2;
		System.out.println(n);
		
		n /= 2; //n = n/2;
		System.out.println(n);

	}

}
