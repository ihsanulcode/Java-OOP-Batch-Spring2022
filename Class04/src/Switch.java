
public class Switch {

	public static void main(String[] args) {
		//Testing switch cases
		
		int result = 3;
		
		switch(result) {
			case 1:
				System.out.println("You are the 1");
				break;
			case 2:
				System.out.println("You are the 2");
				break;
			case 3:
				System.out.println("You are the 3");
				break;
			case 4:
				System.out.println("You are the 4");
				break;
			case 5:
				System.out.println("You are the 5");
				break;
			default:
				System.out.println("You are fake!");
		}
		//rest of the code

	}

}
